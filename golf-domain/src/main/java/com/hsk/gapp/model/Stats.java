package com.hsk.gapp.model;

public class Stats {

	private int putting;
	private int accuracy;
	private int power;
	private int shortgame;

	public int getPutting() {
		return putting;
	}

	public void setPutting(int putting) {
		this.putting = putting;
	}

	public int getAccuracy() {
		return accuracy;
	}

	public void setAccuracy(int accuracy) {
		this.accuracy = accuracy;
	}

	public int getPower() {
		return power;
	}

	public void setPower(int power) {
		this.power = power;
	}

	public int getShortgame() {
		return shortgame;
	}

	public void setShortgame(int shortgame) {
		this.shortgame = shortgame;
	}

}
